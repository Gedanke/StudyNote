def minPath(matrix, i):
    if i == 0:
        return 0
    else:
        distance = 999
        for j in range(i):
            if matrix[j][i] != 0:
                d_tmp = matrix[j][i] + minPath(matrix, j)
                if d_tmp < distance:
                    distance = d_tmp
        return distance


